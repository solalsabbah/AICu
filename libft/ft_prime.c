#include "libft.h"

int			ft_prime(int nb)
{
	int i;

	i = 2;
	if (!nb || nb == 1)
		return (0);
	while (i < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
