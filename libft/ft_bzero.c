#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int				i;
	unsigned char	*ptr;

	ptr = s;
	i = 0;
	while (i < (int)n)
	{
		ptr[i] = '\0';
		i++;
	}
}
