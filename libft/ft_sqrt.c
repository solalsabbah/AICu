#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (!nb)
		return (0);
	while (i * i != nb)
	{
		if (i > nb / 2)
			return (0);
		i++;
	}
	return (i);
}
